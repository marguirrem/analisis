<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;

class BancoController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $bancos = \App\Banco::all();
        return View('Banco.index',compact('bancos'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return View('Banco.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $validatedData = $request->validate([
          'name' => 'required|unique:bancos|min:3|max:255',
          ]);
          $banco = \App\Banco::create([
          'name'=>$request['name'],
          ]);
        return redirect('bancos')->with('status','Banco creado con exito!');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $banco = \App\Banco::findOrFail($id);
        return View('Banco.show',compact('banco'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $banco = \App\Banco::findOrFail($id);
        return View('Banco.edit',compact('banco'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
      $banco = \App\Banco::findOrFail($id);
      $banco->name=$request['name'];
      $banco->save();
      return redirect('bancos/'.$banco->id.'/edit')->with('status','Informaci√≥n actualizada');

    }

